#include "libft.h"

void	ft_putnbr(int n)
{
	long l;

	l = n;
	if (n < 0)
	{
		l = -l;
		ft_putchar('-');
	}
	if (l > 9)
		ft_putnbr(l / 10);
	ft_putchar(l % 10 + 48);
}
