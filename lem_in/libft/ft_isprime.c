#include "libft.h"

int		ft_isprime(int n)
{
	int i;

	i = 3;
	if (n == 2)
		return (1);
	if (n < 2 || n % 2 == 0)
		return (0);
	while (i * i <= n)
	{
		if (n % i == 0)
			return (0);
		i = i + 2;
	}
	return (1);
}
