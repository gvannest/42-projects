#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	long l;

	l = n;
	if (n < 0)
	{
		l = -l;
		ft_putchar_fd('-', fd);
	}
	if (l > 9)
		ft_putnbr_fd(l / 10, fd);
	ft_putchar_fd(l % 10 + 48, fd);
}
