int		ft_iterative_factorial(int nb)
{
	int f;

	f = nb;
	if (nb == 0)
		return (1);
	if (nb < 0 || nb > 12)
		return (0);
	while (nb > 1)
	{
		f = f * (nb - 1);
		nb = nb - 1;
	}
	return (f);
}
