void	disparition(char *str)
{
	int src = 0;
	int dst = 0;

	while (str[src] != '\0')
	{
		if (str[src] != 'e' || str[src] != 'E')
		{
			str[dst] = str[src];
			dst++;
		}
		src++;
	}
}

