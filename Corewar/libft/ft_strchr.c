#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	size_t	i;

	i = 0;
	while (*(s + i) != (char)c && *(s + i) != '\0')
		i++;
	if (*(s + i) == '\0' && *(s + i) != c)
		return (0);
	return ((char*)(s + i));
}
