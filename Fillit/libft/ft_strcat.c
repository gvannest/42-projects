#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	size_t	len;
	size_t	a;

	a = 0;
	len = ft_strlen(s1);
	while (s2[a] != '\0')
	{
		s1[len] = s2[a];
		len++;
		a++;
	}
	s1[len] = '\0';
	return (s1);
}
