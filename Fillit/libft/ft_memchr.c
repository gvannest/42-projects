#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	a;

	a = 0;
	while (a < n)
	{
		if (((char*)s)[a] == (char)c)
			return (&((void*)s)[a]);
		a++;
	}
	return (0);
}
